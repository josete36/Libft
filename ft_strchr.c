#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] != c)
			i++;
		else
			return ((char *)(s + i));
	}
	return (0);
}
